    public void setPosicao() {
        Dimension d = this.getDesktopPane().getSize();
        this.setLocation((d.width - this.getSize().width) / 2, (d.height - this.getSize().height) / 2);
    }

    public void alterarNumeracaoUsuario() {

        Documento efetuandoAlteracao = new Documento();
        try {

            efetuandoAlteracao.zerarNumeracao(String.valueOf(JCBX_TIPOS_DOCS.getSelectedItem()),
                    "ALTERAÇÃO DE SEQUÊNCIA EFETUADA PELO USUÁRIO", Integer.valueOf(JTF_NOVO_NUMERO.getText()));
            JOptionPane.showMessageDialog(null, "SEQUÊNCIA ALTERADA COM SUCESSO!");
        } catch (SQLException | IOException ex) {
            System.out.println("ERRO: "+ex);
            JOptionPane.showMessageDialog(null, "NÃO FOI POSSIVEL EFETUAR A ALTERAÇÃO!", "ERRO!", JOptionPane.WARNING_MESSAGE);
        }
    }

