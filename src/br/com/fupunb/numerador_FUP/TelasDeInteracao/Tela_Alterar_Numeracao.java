    private void JBT_SALVARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBT_SALVARActionPerformed
        if (JCBX_TIPOS_DOCS.getSelectedItem().equals("Selecionar") || "".equals(JTF_NOVO_NUMERO.getText())) {
            JOptionPane.showMessageDialog(null, "SELECIONE UM TIPO DE DOCUMENTO E INSIRA UM VALOR PARA NOVA SEQUÊNCIA!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);

        } else {
            alterarNumeracaoUsuario();
            JCBX_TIPOS_DOCS.setSelectedItem("Selecionar");
            JTF_NOVO_NUMERO.setText("");
        }
    }//GEN-LAST:event_JBT_SALVARActionPerformed


