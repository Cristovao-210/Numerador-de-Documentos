    public void tabelaListarUsuarios(JTable tabela) throws SQLException {
        DefaultTableModel Tabela = (DefaultTableModel) tabela.getModel();
        Connection conexao = null;
        try {
            conexao = Conecta_MySql.conectarDB();
            PreparedStatement listarTabela = conexao.prepareStatement("SELECT NOME, MATRICULAFUB, EMAIL FROM servidor ORDER BY NOME");
            ResultSet rs = listarTabela.executeQuery();
            while (rs.next()) {
                if (rs.getString("NOME").equals("ADMINISTRADOR")) {

                } else {
                    Tabela.addRow(new String[]{rs.getString("NOME"), rs.getString("MATRICULAFUB"), rs.getString("EMAIL")});
                }
            }
        } catch (IOException | SQLException ex) {
            Logger.getLogger(Tela_Cadastrar_Usuario.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            if (conexao != null) {
                conexao.close();
                System.out.println("Conexão finalizada!");
            }
        }

    }

    public void limparTabela(JTable tabela) {

        DefaultTableModel val = (DefaultTableModel) tabela.getModel();

        for (int i = tabela.getRowCount(); i >= 0; i--) {

            if (i > 0) {
                //JOptionPane.showMessageDialog(null, i);
                val.removeRow(0);

            }

        }
    }
