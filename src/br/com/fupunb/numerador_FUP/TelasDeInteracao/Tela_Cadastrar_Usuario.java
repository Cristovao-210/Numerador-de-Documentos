    private void JBT_SALVARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBT_SALVARActionPerformed
        Servidor cadastrarUsuario = new Servidor();
        cadastrarUsuario.setNome(JTF_NOME.getText());
        cadastrarUsuario.setMatriculaFUB(JTF_MATRICULA.getText());
        cadastrarUsuario.setEmail(JTF_EMAIL.getText());
        JTF_ESPELHO_MATRICULA.setText(cadastrarUsuario.getMatriculaFUB());

        if ("".equals(JTF_NOME.getText()) || "".equals(JTF_MATRICULA.getText()) || "".equals(JTF_EMAIL.getText())) {

            JOptionPane.showMessageDialog(null, "ALGUM DOS SEGUINTES CAMPOS NÃO FOI PREENCHIDO: "
                    + "\n'NOME'\n'MATRICULA'\n'E-MAIL'\n***ESSES CAMPOS SÃO OBRIGATÓRIOS.");
        } else {
            try {
                cadastrarUsuario.cadastroServidor(cadastrarUsuario.getNome(), cadastrarUsuario.getMatriculaFUB(), cadastrarUsuario.getEmail());
                limparTabela(TABELA_USUARIOS_CADASTRADOS);
                tabelaListarUsuarios(TABELA_USUARIOS_CADASTRADOS);
            } catch (IOException | SQLException ex) {
                Logger.getLogger(Tela_Cadastrar_Usuario.class.getName()).log(Level.SEVERE, null, ex);
            }
            limparCampos();
            JBT_INCLUIR.setEnabled(true);
            JBT_SALVAR.setEnabled(false);
            jPasswordF_ESCOLHER_SENHA.setEnabled(true);
            jB_CADASTRAR_SENHA.setEnabled(true);
            desabilitarCampos();

        }
    }//GEN-LAST:event_JBT_SALVARActionPerformed

