    public String buscandoNomeUsuario(String MatriculaUsuario) throws IOException, SQLException {

        Servidor buscarNome = new Servidor();
        buscarNome.setMatriculaFUB(MatriculaUsuario);
        String buscarInformacoes = "SELECT NOME FROM servidor WHERE MATRICULAFUB = ?";
        String nomeUsuario = "";
        Connection conexao = Conecta_MySql.conectarDB();
        try {
            PreparedStatement nome = conexao.prepareStatement(buscarInformacoes);
            nome.setString(1, buscarNome.getMatriculaFUB());
            ResultSet rs = nome.executeQuery();
            while (rs.next()) {
                nomeUsuario = rs.getString("NOME");
            }
        } finally {
            if (conexao != null) {
                conexao.close();
                System.out.println("Banco desconectado");
            }
        }
        return nomeUsuario;
    }

