    public void conferindoStatusSenha(String matricula) throws SQLException, IOException {

        Servidor manipularStatusSenha = new Servidor();
        manipularStatusSenha.setMatriculaFUB(matricula);
        String buscarInformacoesStatus = "SELECT STATUSSENHA FROM servidor WHERE MATRICULAFUB = ?";
        Connection conexao = Conecta_MySql.conectarDB();
        try {

            PreparedStatement verificarStatus = conexao.prepareStatement(buscarInformacoesStatus);
            verificarStatus.setString(1, manipularStatusSenha.getMatriculaFUB());
            ResultSet rs = verificarStatus.executeQuery();
            boolean statusOK = false;
            while (rs.next()) {

                statusOK = manipularStatusSenha.verificarStatusSenha(rs.getInt("STATUSSENHA"));
            }
            if (statusOK) {

            } else {
                JOptionPane.showMessageDialog(null, "VOCÊ AINDA NÃO POSSUI SENHA CADASTRADA! CLIQUE EM 'CADASTRAR-SE'.");
                jTF_USUARIO_FUB.setText("");
                jPasswordF_SENHA.setText("");
                jTF_USUARIO_FUB.requestFocus();

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "NÃO FOI POSSÍVEL ACESSAR A BASE DE DADOS!\n"
                    + "VERIFIQUE SE OS DADOS DE IDENTIFICAÇÃO ESTÃO CORRETOS.");
            System.out.println(ex);
        } finally {
            if (conexao != null) {
                conexao.close();
                System.out.println("Banco desconectado");
            }
        }

    }

