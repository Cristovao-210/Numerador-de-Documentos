    public static void iniciarNumerador() {

        Documento verificarAnoVigente = new Documento();

        try {
            if (verificarAnoVigente.consultarAnoVigente()) {
                System.out.println("A numeração não precisa ser reiniciada.");

            } else {
                verificarAnoVigente.zerarNumeracao("CARTA", "REINICIANDO CONTAGEM DEVIDO A VIRADA DE ANO", 0);
                verificarAnoVigente.zerarNumeracao("MEMORANDO", "REINICIANDO CONTAGEM DEVIDO A VIRADA DE ANO", 0);
                verificarAnoVigente.zerarNumeracao("OFÍCIO", "REINICIANDO CONTAGEM DEVIDO A VIRADA DE ANO", 0);
                JOptionPane.showMessageDialog(null, "DEVIDO A MUDANÇA DE ANO A NUMERÇÃO FOI REINICIADA.");
            }

        } catch (IOException | SQLException ex) {
            System.out.print(ex);
            JOptionPane.showMessageDialog(null, "NÃO FOI POSSÍVEL INICIAR O NÚMERADOR. TENTE NOVAMENTE MAIS TARDE!",
                    "FALHA NA CONEXÃO COM A BASE DE DADOS", JOptionPane.ERROR_MESSAGE);
            System.exit(0);
        }

    }

